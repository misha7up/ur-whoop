/**
 * Информация о диагностическом коде для отображения пользователю.
 *
 * @param title    Краткое название неисправности (что сломалось).
 * @param causes   Вероятные причины (почему это возникло).
 * @param repair   Рекомендуемые действия (что делать сервису).
 * @param severity Приоритет: 1=незначительное, 2=требует внимания, 3=критично.
 */
data class DtcInfo(
    val title: String,
    val causes: String = "",
    val repair: String = "",
    val severity: Int = 2,
)

/**
