    private val misfireRegex = Regex("P030[1-9]")

    fun buildProblemDescription(code: String, info: DtcInfo): String {
        val base = baseDtcCode(code)
        problemDescriptions[base]?.let { return it }

        if (base.matches(misfireRegex)) {
            val cyl = base.last()
            return "пропуск зажигания в цилиндре №$cyl, двигатель трясётся, нужна замена свечи зажигания и катушки цилиндра №$cyl"
        }

        return "${info.title.lowercase().trimEnd('.')}. рекомендуется: ${info.repair.lowercase().trimEnd('.')}"
    }

    // ─────────────────────────── URL BUILDER (Uri.Builder) ───────────────────

    fun buildUremontUrl(
        profile: CarProfile,
        vehicleInfo: VehicleInfo?,
        code: String,
        info: DtcInfo,
    ): String {
        val carParts = mutableListOf<String>()

        vehicleInfo?.detectedMake?.let { carParts.add(it) }
        vehicleInfo?.detectedYear?.let { carParts.add(it) }

        if (profile is CarProfile.Manual) {
            if (carParts.isEmpty()) carParts.add(profile.make)
            if (profile.model.isNotBlank()) carParts.add(profile.model)
            if (vehicleInfo?.detectedYear == null && profile.year.isNotBlank()) carParts.add(profile.year)
        }

        if (carParts.isEmpty()) carParts.add("автомобиль")

        val problem = buildProblemDescription(code, info)
        val query = "${carParts.joinToString(" ")} $problem"

        return Uri.Builder()
            .scheme(AppConfig.MAP_SCHEME)
            .authority(AppConfig.MAP_HOST)
            .appendQueryParameter(AppConfig.MAP_QUERY_AI, query)
            .build()
            .toString()
    }
