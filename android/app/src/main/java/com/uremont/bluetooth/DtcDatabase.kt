    /**
     * Код DTC без суффикса FTB из UDS (например `P0420-17` → `P0420`) для справочника и AI-текста.
     */
    internal fun baseDtcCode(code: String): String =
        code.substringBefore("-", missingDelimiterValue = code).uppercase()

    /**
     * Возвращает [DtcInfo] для указанного кода.
     *
     * Приоритет:
     *   1. Manufacturer-specific (BMW, VAG, Mercedes) — если профиль Manual с маркой.
     *   2. Toyota / Lexus — если марка из ручного профиля или [detectedMake] из VIN (в т.ч. JDM `JT*`…).
     *   3. Универсальная таблица — SAE J2012 стандарт.
     *
     * Суффикс `-XX` после UDS 0x19 (Failure Type Byte) отбрасывается при поиске в таблицах.
     *
     * @param detectedMake подпись из WMI (`Toyota car`, `Toyota MPV/SUV`…) — передавайте из [VehicleInfo.detectedMake].
     */
    fun dtcInfo(code: String, profile: CarProfile, detectedMake: String? = null): DtcInfo {
        val base = baseDtcCode(code)
        if (profile is CarProfile.Manual) {
            val make = profile.make.lowercase()
            val specific: DtcInfo? = when {
                "bmw" in make -> bmwDtcInfo(base)
                "volkswagen" in make || "vw" in make || "audi" in make || "skoda" in make || "seat" in make -> vagDtcInfo(base)
                "mercedes" in make -> mercedesDtcInfo(base)
                else -> null
            }
            if (specific != null) return specific
        }
        if (isToyotaLexusContext(profile, detectedMake)) {
            toyotaLexusDtcInfo(base)?.let { return it }
        }
        return universalDtcInfo(base)
    }

    private fun isToyotaLexusByMake(text: String?): Boolean {
        val s = text?.lowercase() ?: return false
        return "toyota" in s || "lexus" in s || "тойота" in s || "лексус" in s
    }

    /** Ручной профиль «Toyota» / «Lexus» или автоопределение по VIN (японские и экспортные WMI). */
    private fun isToyotaLexusContext(profile: CarProfile, detectedMake: String?): Boolean {
        if (profile is CarProfile.Manual) {
            val m = profile.make.lowercase()
            if ("toyota" in m || "lexus" in m || "тойота" in m || "лексус" in m) return true
        }
        return isToyotaLexusByMake(detectedMake)
    }

